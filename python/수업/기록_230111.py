# 저장-리스트(lst)
# 배열과 리스트는 다른 것이지만, 싸피 교육간 설명 할 때 혼용해서 쓸 거임
# 리스트를 만드는 이유: 변수가 여러개여서 데이터를 여러개 저장 가능. 한꺼번에 처리하려고 만듬.
# 리스트를 만들면 데이터는 많지만 변수의 이름은 하나임.
# 그래서 리스트의 데이터를 따로 사용하기 위해 사용하는 것이 'Index' 이다. 번호는 0번부터 순차적으로 늘어남.
# 예: dust[1] = 5, print(dust[1])

