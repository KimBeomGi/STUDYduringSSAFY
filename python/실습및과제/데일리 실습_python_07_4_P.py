# [문제풀이]
# 1. 10분단위 대여에 대한 것과 30분 단위 보험료에 대한 내용을 작성
# 2. 100km 이하시 170(원) * distance(km) + 525(원) * 30분당 보험료 + 1,200(원)*10분당 대여료
# 2-1  100km 초과시 170(원) * 100(km) + 85(원)*distance-100(km) + 525(원) * 30분당 보험료 + 1,200(원)*10분당 대여료

from math import *

def fee(min, distance):
    lental_fee = ceil(min/10)
    insurance = ceil(min/30)
    if distance > 100:
        return (170*100)+85*(distance-100)+(525*insurance)+(1200*lental_fee)
    else:
        return (170*distance)+(525*insurance)+(1200*lental_fee)

print(fee(600,50))
print(fee(600,110))